#include<iostream>
using namespace std;

int main()
  {
      int a;
      cout<<"Size of integer : "<<sizeof(a)<<endl;

      float b;
      cout<<"Size of float : "<<sizeof(b)<<endl;

      bool c;
      cout<<"Size of bool : "<<sizeof(c)<<endl;

      long int li;
      cout<<"SIze of long int: "<<sizeof(li)<<endl;

      short int si;
      cout<<"size of short int: "<<sizeof(si)<<endl;

      double d;
      cout<<"size of double : "<<sizeof(d)<<endl;

      long double ld;
      cout<<"size of long double: "<<sizeof(ld)<<endl;

      char w;
      cout<<"size of character: "<<sizeof(w)<<endl;

      string s;
      cout<<"size of string: "<<sizeof(s)<<endl;

      return 0;
  }